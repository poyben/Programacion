public class practica31 {

    /*
    Práctica 31: Hacer un programa que muestre la tabla de multiplicar del número 5 usando
un bucle for
     */
    public static void main(String[] args) {

        int i = 1;
        for (i = 1; i < 11; i += 1) {
            System.out.println("5 * " + i + " = " + i * 5);
        }

    }

}
